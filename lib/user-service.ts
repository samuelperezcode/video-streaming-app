import { db } from '@/lib/db'

export const getUserByUsername = async (username: string) => {
  const user = await db.user.findUnique({
    where: {
      username
    }
  })

  return user
}

export const getUserById = async ({ id }: { id: string }) => {
  const user = await db.user.findUnique({
    where: { id }
  })

  return user
}
